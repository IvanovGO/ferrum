#include <stdio.h>


int main(){

//Розмір відеосторінки у граф. режимі = розподільча здатність * біт/піксель

int x=1280;
int y=1024;
int rez = 32;

printf("Розмір відеосторінки у граф. режимі = %i Байт \n",x*y*rez/sizeof(char)); 

/*
Кожному символу на екрані відповідають 2 байти у пам’яті. Кожний байт з номером ASCII-кодом символа – байт-символ, його колір – байт-атрибут. 
*/

x=80;
y=25;
int attr=2;
printf("Розмір відеосторінки у текстовому режимі = %i байт\n",x*y*attr); 

return 0;
}
